const textSources = new WeakMap<Text, string>()
const attrSources = new WeakMap<Element, Map<string, string>>()

function normalizeLocale(value: unknown): UiLocale {
  return value === "kk" ? "kk" : "ru"
}

function sourceKey(value: string) {
  return value.replace(/\s+/g, " ").trim()
}

export function translateUiText(value: string, locale: UiLocale): string {
  if (locale === "ru") return value
  const key = sourceKey(value)
  if (!key) return value
  const exact = kk[key]
  if (exact) return value.replace(key, exact)

  const dynamic = key
    .replace(/^Топ (\d+)$/, "Үздік $1")
    .replace(/^(\d+)\s+заданий$/, "$1 тапсырма")
    .replace(/^(\d+)\s+вопросов$/, "$1 сұрақ")
    .replace(/^(\d+)\s+вопр\.$/, "$1 сұрақ")
    .replace(/^(\d+)\s+мин$/, "$1 мин")
    .replace(/^(\d+)\s+тестов$/, "$1 тест")
    .replace(/^Вопрос\s+(\d+)$/, "$1-сұрақ")
    .replace(/^Раздел\s+(\d+)$/, "$1-бөлім")
    .replace(/^Действует до\s+(.+)$/, "$1 дейін жарамды")
    .replace(/^Срок действия:\s+(\d+)\s+(день|дня|дней)$/, "Қолданылу мерзімі: $1 күн")
    .replace(/^Текущий тариф:\s+(.+)$/, "Қазіргі тариф: $1")
    .replace(/^Скидка\s+(.+)$/, "Жеңілдік $1")
    .replace(/^Старая цена$/, "Ескі баға")
    .replace(/^\$\{answered\} из \$\{total\} вопросов\.$/, "${answered} / ${total} сұрақ.")
    .replace(/^Вы ответили на \$\{answered\} из \$\{total\} вопросов\. После завершения вернуться к изменениям нельзя\.$/, "${answered} сұрақтан ${total}-на жауап бердіңіз. Аяқтағаннан кейін өзгерту мүмкін емес.")
    .replace(/^Берём ваши прошлые ошибки и формируем мини-тест на \{limit\} вопросов$/, "Сіздің өткен қателеріңізді алып, {limit} сұрақтық мини-тест құрамыз")
    .replace(/^Выбрано: \$\{profileSubjects\}$/, "Таңдалған: ${profileSubjects}")
    .replace(/^Профиль: \$\{program\.profileSubjects\}$/, "Профиль: ${program.profileSubjects}")
    .replace(/^\$\{program\.universityCount\} вузов$/, "${program.universityCount} ЖОО")
    .replace(/^Ваш балл: \$\{total\} · Сравниваем с пороговыми баллами прошлых лет$/, "Сіздің баллыңыз: ${total} · Өткен жылдардың шекті балдарымен салыстырамыз")
    .replace(/^\$\{passing\} из \$\{programs\.length\} специальностей подходят$/, "${programs.length} мамандықтан ${passing} сәйкес келеді")
    .replace(/^Код \$\{u\.universityCode\} · Порог \$\{u\.displayedMinScore \?\? \"—\"\}$/, "Код ${u.universityCode} · Шекті ${u.displayedMinScore ?? \"—\"}")
    .replace(/^Правильных ответов: \$\{overallCorrect\} из \$\{overallTotal\}$/, "Дұрыс жауаптар: ${overallCorrect} / ${overallTotal}")
    .replace(/^Язык: \$\{session\.language\}$/, "Тіл: ${session.language}")

  return dynamic !== key ? value.replace(key, dynamic) : value
}

function translateAttributes(element: Element, locale: UiLocale) {
  for (const attr of ["placeholder", "aria-label", "title", "alt"]) {
    const current = element.getAttribute(attr)
    if (!current || !sourceKey(current)) continue
    let byAttr = attrSources.get(element)
    if (!byAttr) {
      byAttr = new Map()
      attrSources.set(element, byAttr)
    }
    if (!byAttr.has(attr)) byAttr.set(attr, current)
    const source = byAttr.get(attr) ?? current
    const next = translateUiText(source, locale)
    if (current !== next) element.setAttribute(attr, next)
  }
}

function shouldSkipElement(element: Element | null) {
  if (!element) return false
  return Boolean(
    element.closest(
      "script,style,noscript,svg,code,pre,.katex,.rich-text,[data-no-translate]",
    ),
  )
}

function translateTextNode(node: Text, locale: UiLocale) {
  if (!sourceKey(node.nodeValue || "")) return
  const parent = node.parentElement
  if (shouldSkipElement(parent)) return
  if (!textSources.has(node)) textSources.set(node, node.nodeValue || "")
  const source = textSources.get(node) ?? node.nodeValue ?? ""
  const next = translateUiText(source, locale)
  if (node.nodeValue !== next) node.nodeValue = next
}

function translateSubtree(root: ParentNode, locale: UiLocale) {
  if (root instanceof Element) translateAttributes(root, locale)
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT | NodeFilter.SHOW_ELEMENT)
  let current: Node | null = walker.currentNode
  while (current) {
    if (current.nodeType === Node.TEXT_NODE) {
      translateTextNode(current as Text, locale)
    } else if (current.nodeType === Node.ELEMENT_NODE) {
      translateAttributes(current as Element, locale)
    }
    current = walker.nextNode()
  }
}

type I18nContextValue = {
  locale: UiLocale
  setLocale: (locale: UiLocale, options?: { syncProfile?: boolean }) => Promise<void>
  t: (source: string) => string
}

const I18nContext = createContext<I18nContextValue | null>(null)

function getInitialLocale(): UiLocale {
  if (typeof window === "undefined") return "ru"
  const urlLocale = new URLSearchParams(window.location.search).get("lang")
  if (SUPPORTED_LOCALES.includes(urlLocale as UiLocale)) return urlLocale as UiLocale
  const stored = window.localStorage.getItem(STORAGE_KEY)
  if (SUPPORTED_LOCALES.includes(stored as UiLocale)) return stored as UiLocale
  return window.navigator.language.toLowerCase().startsWith("kk") ? "kk" : "ru"
}

export function UiI18nProvider({ children }: { children: ReactNode }) {
  const { user, isAuthenticated, refresh } = useAuth()
  const [locale, setLocaleState] = useState<UiLocale>(getInitialLocale)
  const syncInFlight = useRef(false)

  useEffect(() => {
    const preferred = normalizeLocale(user?.preferredLanguage)
    const stored = typeof window !== "undefined" ? window.localStorage.getItem(STORAGE_KEY) : null
    if (user?.preferredLanguage && !stored && preferred !== locale) {
      setLocaleState(preferred)
    }
  }, [locale, user?.preferredLanguage])

  useEffect(() => {
    if (!isAuthenticated || !user?.id || syncInFlight.current) return
    const preferred = normalizeLocale(user.preferredLanguage)
    const stored =
      typeof window !== "undefined" ? window.localStorage.getItem(STORAGE_KEY) : null
    if (!stored || preferred === locale) return

    syncInFlight.current = true
    void api("/users/me", {
      method: "PATCH",
      body: { preferredLanguage: locale },
    })
      .then(() => refresh())
      .catch(() => {})
      .finally(() => {
        syncInFlight.current = false
      })
  }, [isAuthenticated, locale, refresh, user?.id, user?.preferredLanguage])

  useEffect(() => {
    document.documentElement.lang = locale
    translateSubtree(document.body, locale)
    const observer = new MutationObserver((records) => {
      for (const record of records) {
        if (record.type === "characterData" && record.target.nodeType === Node.TEXT_NODE) {
          translateTextNode(record.target as Text, locale)
          continue
        }
        if (record.type === "attributes" && record.target.nodeType === Node.ELEMENT_NODE) {
          translateAttributes(record.target as Element, locale)
          continue
        }
        record.addedNodes.forEach((node) => {
          if (node.nodeType === Node.TEXT_NODE) translateTextNode(node as Text, locale)
          if (node.nodeType === Node.ELEMENT_NODE) translateSubtree(node as Element, locale)
        })
      }
    })
    observer.observe(document.body, {
      childList: true,
      subtree: true,
      characterData: true,
      attributes: true,
      attributeFilter: ["placeholder", "aria-label", "title", "alt"],
    })
    return () => observer.disconnect()
  }, [locale])

  const setLocale = useCallback(
    async (nextLocale: UiLocale, options?: { syncProfile?: boolean }) => {
      const next = normalizeLocale(nextLocale)
      setLocaleState(next)
      window.localStorage.setItem(STORAGE_KEY, next)
      if (isAuthenticated && options?.syncProfile !== false) {
        try {
          syncInFlight.current = true
          await api("/users/me", {
            method: "PATCH",
            body: { preferredLanguage: next },
          })
          await refresh()
        } catch {
          // The UI language should still switch even if profile sync fails.
        } finally {
          syncInFlight.current = false
        }
      }
    },
    [isAuthenticated, refresh],
  )

  const value = useMemo<I18nContextValue>(
    () => ({
      locale,
      setLocale,
      t: (source) => translateUiText(source, locale),
    }),
    [locale, setLocale],
  )

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>
}

export function useUiI18n() {
  const ctx = useContext(I18nContext)
  if (!ctx) throw new Error("useUiI18n must be used within UiI18nProvider")
  return ctx
}
